# Cria a variável tempoExperiencia e atribui o valor 3
tempoExperiencia = 3

# Verifica se o valor da variável tempoExperiencia é menor que 2
if tempoExperiencia < 2:
    print('Nível de conhecimento júnior.')
# Verifica se o valor da variável tempoExperiencia é maior que 2 e menor que 5
elif 2 < tempoExperiencia < 5:
    print('Nível de conhecimento pleno.')
# Caso contrário, imprime 'Nível de conhecimento sênior.'
else:
    print('Nível de conhecimento sênior.')
